package tickers;

public class InfoData {

	private int coins_num;
    private long time;
    
	public int getCoins_num() {
		return coins_num;
	}
	public void setCoins_num(int coins_num) {
		this.coins_num = coins_num;
	}
	public long getTime() {
		return time;
	}
	public void setTime(long time) {
		this.time = time;
	}
    
}
